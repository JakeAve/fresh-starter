export function generateNickname() {
  const advIdx = Math.floor(Math.random() * adverbs.length);
  const adjIdx = Math.floor(Math.random() * adjectives.length);
  const nounIdx = Math.floor(Math.random() * nouns.length);
  const num = Math.floor(Math.random() * 100);

  return `${adverbs[advIdx]}-${adjectives[adjIdx]}-${nouns[nounIdx]}-${num}`;
}
